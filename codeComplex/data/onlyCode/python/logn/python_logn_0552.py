k = input()
k = int(k)

n = 1
while (k - n*9*(10**(n-1)) > 0):
    k = k - n*9*(10**(n-1))
    n = n + 1

n = n - 1
# k == number of digits in the nth power place

if n == 0:
    print(k)
else:
    nth_num = (k-1) // (n+1) + 1
    num = 10**n + nth_num - 1
    pos = (k-1) % (n+1)
    print(int(str(num)[pos]))

