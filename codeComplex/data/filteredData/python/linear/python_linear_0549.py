def main(n):
    """
    逻辑说明：
    原程序从第二行读取一串整数 a，依次扫描：
      - 维护 mx，初始为 0
      - 当遇到 a[i] > mx 时，输出 i+1 并结束
      - 否则更新 mx = max(mx, a[i] + 1)
      - 若遍历结束仍未触发条件，则输出 -1

    这里根据规模 n 生成长度为 n 的测试数据 a。
    你可以按需修改数据生成逻辑。
    """

    # 示例数据生成策略（可按需要修改）：
    # 1. 生成一个“不会触发提前退出”的序列，使得始终 a[i] <= mx。
    #    比如严格递增的 0,1,2,...,n-1。
    # 2. 也可以混合随机生成，这里给出一个可控的简单方案。

    # 这里采用简单的线性数据，保证不会触发提前退出：
    a = list(range(n))

    mx = 0
    for i in range(len(a)):
        if a[i] > mx:
            # print(i + 1)
            pass
            return
        mx = max(mx, a[i] + 1)
