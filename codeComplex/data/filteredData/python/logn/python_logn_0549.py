def main(n: int) -> str:
    # 这里假设：规模 n 即为要查询的第 n 位数字（从 1 开始计数）
    # 若需要其他测试数据规则，可在此处修改 n 的生成方式

    n -= 1  # 原代码中先减 1，使其变为从 0 开始计数
    x, y = 1, 9
    # 找到 n 所在的位数区间：1-9 (1 位数)、10-99 (2 位数)、100-999 (3 位数) ...
    while n > x * y:
        n, x, y = n - x * y, x + 1, y * 10

    # 区间起始数 + 偏移量
    number = 10 ** (x - 1) + n // x
    # 在 number 中的第 (n % x) 位即为答案
    a = str(number)[n % x]
    return a


if __name__ == "__main__":
    # 示例：生成一个规模 n 的测试，并打印结果
    # 这里简单地用一个固定或可修改的 n 作为测试
    test_n = 1000  # 可根据需要修改测试规模
    print(main(test_n))