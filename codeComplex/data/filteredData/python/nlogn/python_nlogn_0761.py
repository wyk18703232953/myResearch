def main(n):
    # 解释输入结构：
    # 原程序含义：
    #   第一行：n -> 测试用例数量
    #   每个测试用例两行：
    #       k
    #       k 个整数
    #
    # 本重构中：外层 main(n) 的参数 n 视为“测试用例数量”
    #           每个测试用例的 k 与其对应的数组长度均由 n 和测试编号 i 确定性生成

    t = n  # 测试用例数量

    for i in range(t):
        # 为了可规模化和避免退化到太小的 k，设定：
        # k 随着 n 和 i 增长，且 k >= 2
        # 示例：k = max(2, (i % n) + 2)
        # 当 n 很大时，k 的上限大约是 n+1
        k = max(2, (i % n) + 2)

        # 构造长度为 k 的确定性整数数组
        # 使用简单算术：s[j] = (i + 1) * (j + 1)
        s = [(i + 1) * (j + 1) for j in range(k)]

        s.sort()
        # print(min(k - 2, s[k - 2] - 1))
        pass
if __name__ == "__main__":
    # 示例：以 n = 5 作为输入规模调用
    main(5)