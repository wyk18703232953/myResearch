def main(n):
    # 原逻辑：判断 n 是否能被“幸运数”整除
    l = [4, 7, 47, 74, 44, 77, 447, 444, 474, 777, 747, 744, 477]
    c = 0
    for x in l:
        if n % x == 0:
            c = 1
            break
    if c == 1:
        print("YES")
    else:
        print("NO")


if __name__ == "__main__":
    # 按规模 n 生成测试数据：这里直接使用 n 本身作为待测数字
    # 可以根据需要修改为其他生成方式，例如 n 的倍数、随机数等
    test_n = 1000  # 示例：规模为 1000，可自行调整
    main(test_n)