def main(n):
    # 这里根据 n 生成测试数据：
    # 原程序是从输入读取 n,m，这里我们只用到 n，
    # 所以构造一个与原逻辑兼容的 m（例如 m = n 或任意整数）。
    m = n  # m 在原代码中未被使用，仅为占位

    # 按原逻辑输出
    print("8" * n)
    if n >= 1:
        print("1" * (n - 1) + "2")
    else:
        # 若允许 n = 0，可约定输出为空行或不输出；此处选择输出空行
        print("")

if __name__ == "__main__":
    # 示例：调用 main，规模 n 可在此处修改测试
    main(5)