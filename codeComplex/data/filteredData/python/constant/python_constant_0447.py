import random

def main(n: int):
    """
    根据规模 n 生成测试数据，并打印两行字符串：
    第1行：若干个 '4'，后接一个 '5'
    第2行：若干个 '5'
    生成规则随 n 调整，这里示例为：
      - 第1行长度 = max(1, n)
      - 第2行长度 = max(1, n + 1)
    """
    # 防止 n 非法
    n = max(1, int(n))

    # 根据 n 生成与原程序形式类似的数据
    a = '4' * n + '5'
    b = '5' * (n + 1)

    print(a)
    print(b)


if __name__ == "__main__":
    # 示例：规模为 300 时，与原始程序形式接近（原来是 300 个 '4' 加一个 '5'，
    # 和 301 个 '5'），可以按需修改此处的 n 进行测试
    main(300)