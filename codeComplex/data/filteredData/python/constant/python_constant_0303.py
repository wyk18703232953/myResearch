def main(n):
    # 生成测试数据：
    # 这里假设：k, s, p 随 n 合理变化，你可以按需要调整生成逻辑
    k = max(1, n // 3)   # 例如：使用者数量随规模变化
    s = max(1, n // 5)   # 例如：每包物品容量
    p = max(1, n // 4)   # 例如：一次购买可买的包数

    # 原程序逻辑
    q = (n + s - 1) // s
    ans = (q * k + p - 1) // p
    print(ans)


if __name__ == "__main__":
    # 示例：调用 main，规模 n 自行设定
    main(100)