def main(n: int):
    # 生成 n 组 (i, j) 测试数据，并输出原逻辑的计算结果
    #
    # 这里构造方式为：
    # 第 t 组：i = 1, j = 2t+1  （确保 i >= 1 且 i < j，覆盖奇偶情况）
    # 你可以根据需要自行修改生成规则。
    for t in range(1, n + 1):
        i = 1
        j = 2 * t + 1  # 保证 j > i
        i -= 1  # 与原程序保持一致

        ans = (
            (j // 2 * (j // 2 + 1) - i // 2 * (i // 2 + 1))
            - (((j - j // 2) * (j - j // 2)) - ((i - i // 2) * (i - i // 2)))
        )
        print(ans)


if __name__ == "__main__":
    # 示例：执行 5 组测试
    main(5)