def main(n):
    # n 为规模参数，这里用 n 来控制字符串长度
    # 生成测试数据：根据 n 生成 a 和 b
    # 原逻辑：a 为 1129 个 '8' 加 1 个 '9'，b 为 1130 个 '1'
    # 这里将 1129 和 1130 替换为与 n 相关的长度，例如：
    # a: n-1 个 '8' + '9'
    # b: n 个 '1'
    if n <= 0:
        return

    a = '8' * (n - 1) + '9'
    b = '1' * n

    print(a)
    print(b)


if __name__ == "__main__":
    # 示例：可以根据需要修改 n 的默认测试规模
    main(1130)