def main(n):
    # 在原程序中：
    #   第一行输入为 n
    #   第二行输入为 m
    #
    # 为了进行可规模化的时间复杂度实验，这里约定：
    #   - 参数 n 继续表示位数规模（与原程序第一行含义一致）
    #   - 第二个输入 m 由 n 确定性生成，规模大约与 2^n 同量级，以放大运算代价
    #
    # 构造一个确定性的 m，使得：
    #   1. 与 n 一一对应（无随机性）
    #   2. 数值随 n 增长而增长
    #   3. 简单可计算
    #
    # 这里使用 m = 3 * (2**n) + n 作为确定性构造：
    #   - m % (1<<n) = (3 * 2**n + n) % (2**n) = n
    #   - 这样既保留了原算法 m%(1<<n) 的逻辑，又保证输入完全由 n 决定
    m = 3 * (1 << n) + n

    result = m % (1 << n)
    # print(result)
    pass
if __name__ == "__main__":
    # 示例调用：可以根据需要修改 n 来做规模实验
    main(10)